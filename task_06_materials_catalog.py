# Исходные материалы для примера
items = ["Брус", "Доска", "ОСБ", "Утеплитель", "Саморезы"]

print("=== КАТАЛОГ МАТЕРИАЛОВ ===")
print(f"Исходный список: {items}")
print(f"Первый материал: {items[0]}")
print(f"Последний материал: {items[-1]}")
print(f"Средние элементы: {items[1:4]}")

# Добавляем ещё два элемента в список
items.append("Лак")
items.append("Фанерный лист")

print()
print("После добавления двух материалов:")
print(items)

# Удаляем материал со второй позиции
removed_item = items.pop(1)

print()
print(f"Удалённый второй элемент: {removed_item}")
print(f"Итоговый список: {items}")
print(f"Длина списка: {len(items)}")