# Текущее значение температуры
c_temp = 18

# Переводим температуру в Фаренгейты
f_temp = c_temp * 9 / 5 + 32

# Выясняем состояние воды при такой температуре
if c_temp <= 0:
    state_water = "Лёд"
elif c_temp >= 100:
    state_water = "Пар"
else:
    state_water = "Жидкость"

print("=== КОНВЕРТЕР ТЕМПЕРАТУР ===")
print(f"Температура в Цельсиях: {c_temp} °C")
print(f"Температура в Фаренгейтах: {f_temp:.2f} °F")
print(f"Состояние воды: {state_water}")